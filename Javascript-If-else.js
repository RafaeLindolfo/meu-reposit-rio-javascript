/* CONTROLE DE FLUXO - CONDICIONAIS - IF - ELSE */

// if  ->  "SE" esse código é usado para executar um determinado comando caso ele seja verdadeiro
// else -> "SE NÃO" já essa aqui só da para ser usado junto com o "if" pois caso o "if" não seja verdadeiro, será executado o que está dentro do else

const age = 16
if(age >= 18){
    console.log('Você é maior de idade')
} else {
    console.log('Você não é maior de idade')
}