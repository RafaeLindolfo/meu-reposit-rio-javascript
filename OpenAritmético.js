
/* Operadores aritméticos
+ --> Adição
- --> Subtração
* --> Multiplicação
/ --> Divisão
% --> Resto 
++ -> Incremento
-- -> Decremento
** -> Exponencial
*/


//+ --> Adição
const number1 = 35
const number2 = 100

const result = number1 + number2

console.log(result)
// - -> Subtração
const number3 = 35
const number4 = 100

const result1 = number3 - number4

console.log(result1)

// * --> Multiplicação
const number5 = 35
const number6 = 100

const result2 = number5 * number6

console.log(result2)


// / --> Divisão
const number7 = 35
const number8 = 100

const result3 = number7 / number8

console.log(result3)

// % --> Resto
const number9 = 35
const number10 = 100

const result4 = number9 % number10

console.log(result4)

// ++ -> Incremento
let number11 = 35
number11++
number11++
number11++

console.log(number11)
// Ou pode usar também:

console.log(++number11)
// E o mesmo pode ser usado com "-- -> Decremento"

// ** -> Exponencial

let Number = 20

console.log(Number ** 2)
// Ou pode usar apenas dentro do console.log

console.log(2 ** 3)
