/* OPERADORES DE ATRIBUIÇÃO*/

// =  -> Atribuição
let fistNumber = 10
// +=  -> Adição
fistNumber += 2
console.log(fistNumber)
// -=  -> Subtração
let secondNumber = 10
secondNumber -= 2

console.log(secondNumber)
// /=  -> Divisão
let thirdNumber = 20
thirdNumber /= 2

console.log(thirdNumber)
// *=  -> Multiplicação
let roomNumber = 10
roomNumber *= 3

console.log(roomNumber)
// %=  -> Resto
let fifthnumber = 15
fifthnumber %= 3

console.log(fifthnumber)