/* OPERADORES LÓGICOS*/


/*&&  -> E // Nesse código(conhecido como Filho obediente) funcionar da seguinte forma, se um operador for falso, o resultado sempre será falso. 
    true && true = true ( Quando dois operadores tem uma posição verdadeira o resultado é verdadeiro para o código)
    true && false = false ( Quando um operador é falso e umé verdadeiro, o código tem o resultado como falso)
    false && false = false (Quando dois Operadores é falso, o resultado é falso)
*/    
console.log(true && true)
console.log(true && false)
console.log(false && false)
/*||  -> Ou // Nesse código(conhecido como filho espertinho) funciona da seguinte forma, sempre que tiver um verdadeiro, o resultado sempre será verdadeiro.
    true || true = true ( Quando dois operadores são verdadeiros, o resultado é verdadeiro)
    true || false = true ( Quando um operador é verdadeiro e o outro é falso, o resultado é verdadeiro)
    false || false = false ( Quando os dois operadores são falsos, o resultado é falso)
  */  

    console.log(true || true)
    console.log(true || false)
    console.log(false || false)
/*
!   -> Negação // Nesse código (conhecido como o filho rebelde/ do contra) funciona da seguinte forma sempre vai inverter o verdadeiro para falso ou viceversa.
    !true = false ()
    !false = true*/

    console.log(!true)
    console.log(!false)