/*Operadores de Comparação*/

 
// ==  -> Igual. Esse código compara o valor, mas não compara o tipo de valor Ex:

const fistName = 'Pietro'
const secondName = 'Anthony'

console.log(fistName == secondName)

// ===  -> TOTALMENTE Igual, Esse código verifica o tipo de dado!
const fistNumber = '26'
const secondNumber = 26

console.log(fistNumber === secondNumber)
// !=  -> Diferente // Ele compara o valor, mas não compara o tipo de valor

const fistName1 = 'Pietro'
const secondName1 = 'Anthony'

console.log(fistName1 != secondName1)
// !==  -> TOTALMENTE Diferente // Ele verificar o tipo de dado!

const fistNumber1 = '26'
const secondNumber1 = 26

console.log(fistNumber1 === secondNumber1)

/* OPERADORES DE COMPARAÇÃO PT2*/

// >  -> Maior

const pNumber = 10
const sNumber = 20

console.log(pNumber > sNumber)
// >=  -> Maior ou igual

const pNumber2 = 10
const sNumber2 = 10

console.log(pNumber2 >= sNumber2)
// <  -> Menor

const pNumber1 = 10
const sNumber1 = 20

console.log(pNumber1 < sNumber1)
// <=  ->Menor ou Igual
const pNumber3 = 10
const sNumber3 = 9

console.log(pNumber3 <= sNumber3)